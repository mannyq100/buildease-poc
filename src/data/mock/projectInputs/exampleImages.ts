/**
 * Get a random image for a specific category and type
 * 
 * @param category The image category (building, construction, feature, material)
 * @param type The specific type within the category
 * @returns URL to a random image for the specified category and type
 */
export function getRandomImage(category: 'building' | 'construction' | 'feature' | 'material', type: string): string {
  let images: string[] = [];
  
  switch (category) {
    case 'building':
      images = BUILDING_STYLE_IMAGES[type as keyof typeof BUILDING_STYLE_IMAGES] || 
               BUILDING_STYLE_IMAGES.modern;
      break;
    case 'construction':
      images = CONSTRUCTION_PHASE_IMAGES[type as keyof typeof CONSTRUCTION_PHASE_IMAGES] || 
               CONSTRUCTION_PHASE_IMAGES.completed;
      break;
    case 'feature':
      images = FEATURE_IMAGES[type as keyof typeof FEATURE_IMAGES] || 
               FEATURE_IMAGES.livingRoom;
      break;
    case 'material':
      images = MATERIAL_IMAGES[type as keyof typeof MATERIAL_IMAGES] || 
               MATERIAL_IMAGES.concrete;
      break;
  }
  
  return images[Math.floor(Math.random() * images.length)];
}

